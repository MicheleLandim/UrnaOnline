package dados;

public class Validacao {

	int titulo;
	String zona;
	String secao;
	String permite_votar;

	public int getTitulo() {
		return titulo;
	}

	public void setTitulo(int titulo) {
		this.titulo = titulo;
	}

	public String getZona() {
		return zona;
	}

	public void setZona(String zona) {
		this.zona = zona;
	}

	public String getSecao() {
		return secao;
	}

	public void setSecao(String secao) {
		this.secao = secao;
	}

	public String getPermite_votar() {
		return permite_votar;
	}

	public void setPermite_votar(String permite_votar) {
		this.permite_votar = permite_votar;
	}

}
